defined('MOODLE_INTERNAL') || die();

global $DB;
user_preference_allow_ajax_update('drawer-open-nav', PARAM_ALPHA);
require_once($CFG->libdir . '/behat/lib.php');

if (isloggedin()) {
    $navdraweropen = (get_user_preferences('drawer-open-nav', 'true') == 'true');
} else {
    $navdraweropen = false;
}
$extraclasses = [];
if ($navdraweropen) {
    $extraclasses[] = 'drawer-open-left';
}
$domain_get=explode('.', @$_SERVER['HTTP_HOST']);
$domain = $domain_get[0];

$bodyattributes = $OUTPUT->body_attributes($extraclasses);
$blockshtml = $OUTPUT->blocks('side-pre');
$hasblocks = strpos($blockshtml, 'data-block=') !== false;
$buildregionmainsettings = !$PAGE->include_region_main_settings_in_header_actions();
// If the settings menu will be included in the header then don't add it here.
$regionmainsettingsmenu = $buildregionmainsettings ? $OUTPUT->region_main_settings_menu() : false;
$templatecontext = [
    'sitename' => format_string($SITE->shortname, true, ['context' => context_course::instance(SITEID), "escape" => false]),
    'output' => $OUTPUT,
    'sidepreblocks' => $blockshtml,
    'hasblocks' => $hasblocks,
    'bodyattributes' => $bodyattributes,
    'navdraweropen' => $navdraweropen,
    'regionmainsettingsmenu' => $regionmainsettingsmenu,
    'hasregionmainsettingsmenu' => !empty($regionmainsettingsmenu)
];
// $urlcolumn = $DB->sql_compare_text('url');

// if ($DB->record_exists('theme_detail', [$urlcolumn => $domain])) {
if ($DB->record_exists_sql("SELECT * FROM {theme_detail} WHERE url = '$domain'")) {+
    $themerecord = $DB->get_record_sql("SELECT td.* FROM {theme_detail} td WHERE td.url = '$domain'");
     $templatecontext['btn_color'] = $themerecord->theme_color;
    $menurecord = $DB->get_records('menu_configuration',['theme_id' =>$themerecord->id]);
    $menuitems[0] = "";
    if ($menurecord) {
        foreach($menurecord as $menuvalue){
            $menuitems[$menuvalue->menu_index] = ['menuname' => $menuvalue->menu_name, 'menuurl' => $menuvalue->menu_url];
        }
        $templatecontext['menus'] = $menuitems;
    }
}

$nav = $PAGE->flatnav;
$templatecontext['flatnavigation'] = $nav;
$templatecontext['firstcollectionlabel'] = $nav->get_collectionlabel();
echo $OUTPUT->render_from_template('theme_boost/columns2', $templatecontext);

